fun maxSubArray(nums: IntArray): Int {
    var maxSun = nums[0]//åº”å¯¹å…ƒç´ åªæœ‰ä¸€ä¸ªçš„æƒ…å†µ
    var sun = 0
    for (num in nums) {
        if (sun > 0) {
            sun += num
