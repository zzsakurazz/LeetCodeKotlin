fun merge(nums1: IntArray, m: Int, nums2: IntArray, n: Int) {
    var t1 = m - 1
    var t2 = n - 1
    var t = n + m - 1
    while (t1 >= 0 && t2 >= 0)
        nums1[t--] = if (nums1[t1] >= nums2[t2]) nums1[t1--] else nums2[t2--]
    while (t2 >= 0) nums1[t--] = nums2[t2--]
}
